<p>Nuclio.ch est une plateforme d’édition numérique dont l’objectif est de documenter l’histoire du nucléaire en Suisse. Sources, analyses, chronologie, bibliographie : l’ensemble de nos ressources permettent d’explorer les débats, les choix technologiques et politiques, ainsi que les impacts du nucléaire sur le territoire helvétique.</p>

<p>Réalisée dans le cadre d’un séminaire de Bachelor de l’Université de Genève, cette plateforme s’adresse à toutes personnes (étudiante, enseignante, chercheuse ou non) souhaitant bénéficier d’une approche rigoureuse, critique et accessible de cette histoire, et d’un accès direct aux documents qui l’ont incarnée.</p>

